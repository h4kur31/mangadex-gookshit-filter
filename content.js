const getLocalStorage = () => {
  let data = localStorage.getItem(localStorageKey);
  try {
    data = JSON.parse(data);
  } catch (_) {
    // ignore errors
  }
  return data;
};
const setLocalStorage = (data) => localStorage.setItem(localStorageKey, JSON.stringify(data));

const updateIds = (url, callback) => {
  if (!Array.isArray(gookshitIds)) {
    gookshitIds = [];
  }

  const doCallback = () => {
    if (callback instanceof Function) {
      callback();
    }
  };

  const xhr = new XMLHttpRequest();
  xhr.responseType = "document";

  xhr.open("GET", url);
  xhr.onload = () => {
    const doc = xhr.response;
    if (!doc) {
      doCallback();
      return;
    }

    const entries = Array.from(doc.getElementsByClassName("manga-entry"));
    if (!entries || !entries.length) {
      doCallback();
      return;
    }

    entries.forEach((entry) => {
      const link = entry.querySelector("a[href^='/title/']");
      if (!link || !link.href) {
        return;
      }
      const matches = link.href.match(/title\/(\d+)\//);
      if (!matches || !matches.length || matches.length < 2) {
        return;
      }
      if (!gookshitIds.includes(matches[1])) {
        gookshitIds.push(matches[1]);
      }
    });

    const pagination = doc.querySelector(".pagination");
    if (!pagination) {
      doCallback();
      return;
    }

    const activePage = pagination.querySelector(".page-item.active");
    if (!activePage) {
      doCallback();
      return;
    }

    const nextPage = activePage.nextElementSibling;
    if (!nextPage || !nextPage.classList || nextPage.classList.contains("disabled")) {
      doCallback();
      return;
    }

    const nextPageUrl = nextPage.querySelector("a").href;
    setTimeout(() => updateIds(nextPageUrl, callback), 6000);
  };

  xhr.onerror = () => {
    setTimeout(() => updateIds(url, callback), 6000);
  };
  xhr.send();
};

const onDOMContentLoaded = () => {
  const style = document.createElement("style");
  style.textContent = ".hideBtn{cursor: pointer;margin-right: 6px;}#content .hideBtn{margin-top: 3px;}.hideBtn:hover{color: #fff;}";
  (document.head || document.body).appendChild(style);

  const links = Array.from(document.querySelectorAll("a[href^='/title/']"));
  links.forEach((link) => {
    const matches = link.href.match(/title\/(\d+)\//);
    if (!matches || matches.length < 2) {
      return;
    }

    const id = matches[1];
    if (!id || !id.length) {
      return;
    }

    const parent = link.closest(".col-md-6.border-bottom") || link.closest(".list-group-item") || link.closest(".manga-entry") || link.closest(".owl-item");
    const removeParent = () => {
      if (parent && parent.remove) {
        parent.remove();
      }
    };

    if (gookshitIds.includes(id)) {
      removeParent();
    } else if (link.textContent && link.textContent.length && link.classList.contains("manga_title")) {
      const hideBtn = document.createElement("span");
      hideBtn.classList.add("fas", "fa-times", "hideBtn");
      hideBtn.addEventListener("click", () => {
        let data = getLocalStorage();
        if (!Array.isArray(data)) {
          data = [];
        }
        data.push(id);

        setLocalStorage(data);
        removeParent();
      });

      link.parentElement.insertBefore(hideBtn, link);
    }
  });
};

